'These scripts is used to identified nonlinear dynamic systems,'
'and the method is based on the following paper.'
'''A paremeter Optimization Method for Radial Basis Function Type Models'''
'which can be downloaded at the IEEE transaction site.'

'''Training.m'' trains a RBF-ARX model based data.'
'''Validating.m'' validates the model, and plot error figure.'
'''ObjFun.m'' is used in ''Training.m'' to create a nonlinear least square'
'mean function.'
'''LSM_Data.m'' is used in ''ObjFun.m'' to prepare data for LSM'
'so that the SNPOM can be implemented.'

